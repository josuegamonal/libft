char	*ft_strchr(const char *s, int c)
{
	int 	i;
	char *s2;
	s2 = (char *)s;
	i = 0;
	while (s2[i] != '\0')
	{
		if (s2[i] == '\0')
			return (s[i]);
		if (s2[i] == c)
			return (s[i]);
	i++;
	}
	return (s[i]);
}
